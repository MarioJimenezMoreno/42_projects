#include "libft.h"

int ft_memcmp(const void *s1, const void *s2, size_t n)
{
	size_t	i;

	i = 0;
	if (n == 0)
	{
		return (0);
	}
	while (i < n)
	{
		if (((char *)s1)[i] != ((char *)s2)[i])
			break ;
		i++;
		n--;
	}
	return ((int)(((char *)s1)[i] - ((char *)s2)[i]));
}
