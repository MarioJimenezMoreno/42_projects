#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	num;

	if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
	}
	if (nb <= 2147483647 && nb >= -2147483647)
	{
		if (nb < 0)
		{
			write(1, "-", 1);
			nb *= -1;
		}
		if (nb / 10)
			ft_putnbr(nb / 10);
		num = (nb % 10) + '0';
		write (1, &num, 1);
	}
}
