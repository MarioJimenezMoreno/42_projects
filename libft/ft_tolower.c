#include "libft.h"

int		ft_tolower(int c)
{
	int		i;

	i = 0;
	while (c[i])
	{
		if (c[i] >= 'A' && c[i] <= 'Z')
			c[i] = c[i] + 32;
		i++;
	}
	return (c);
}
