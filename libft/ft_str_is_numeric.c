#include <unistd.h>

int	ft_str_is_numeric(char *str)
{
	if (*str > 0)
	{
		while (*str != '\0')
		{
			if (*str < 48 || *str > 57)
			{
				return (0);
			}
			str++;
		}
	}
	return (1);
}
